package Array_13;

public class MUltiArray2 {

	public static void main(String[] args) {
		 
			//declaring and initializing 2 array  
			int arr[][]={{1,2,3},{2,4,5},{4,4,5}};  
			
			//printing 2 array  
			
			for(int i=0;i<3;i++)// for row
			{  
				for(int j=0;j<3;j++)// for columns
			 {  
					System.out.print(arr[i][j]+" ");  
			 }  
					System.out.println();  		
					}  
		}

	}


