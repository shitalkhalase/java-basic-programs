package Loopsconcept_4_topic;

public class While_loop_concept_1 {

	public static void main(String[] args) {
		int a=1; //initialization part
		while(a<=10) //conditional part
		{
			System.out.println(a); //code 
			a++;  // incremental part //a=a+1
		}
	}

}
