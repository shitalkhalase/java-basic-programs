package corejava_1_topic;// package- keyword

public class Demo_datatypes_1 { //public-keyword-access modifier
								//class- keyword

	public static void main(String[] args) {
		//static-keyword
		//main-method-it is execution point of our program
		//comments-documentation regarding our code
	
		Boolean b =true;// 1 bit
		byte bt =-128;//8 bit
		short st=-32768;//16 bit
		int roll =123456789;//32 bit
		long l= 7777446464L;//64 bit
		float ft= 505.45454f;//32 bit
		double d=44444.44448;//64 bit,for fractional values
		double d1=100;//we can store value int but vice versa is not true
		char alphabets='a';// 16 bit
		String name="Java";//not data type,its a class.but we 
		        //can use as a data type to define data values
	
	
	
		System.out.println(b);// used for o/p in console
		System.out.println(bt);
		System.out.println(st);
		System.out.println(roll);
		System.out.println(l);
		System.out.println(ft);
		System.out.println(d);
		System.out.println(d1);
		System.out.println(alphabets);
		System.out.println(name);
	    // system-class
		//out-static member of system class
		//println()-method
	
	}

} 
