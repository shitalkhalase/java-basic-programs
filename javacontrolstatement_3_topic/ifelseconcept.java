public class ifelseconcept {

	public static void main(String[] args) {
		int i=10;
		if(i>11)
		{
			System.out.println("This works");//it will only execute when condition will be true
		}
		else 
		{
			System.out.println("This is not works");// when condition will false
		}
	}

}
